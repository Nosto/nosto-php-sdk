namespace Nosto\Model\Category;

use Nosto\Mixins\HtmlEncoderTrait;
use Nosto\Types\CategoryInterface;
use Nosto\AbstractObject;
use Nosto\Types\HtmlEncodableInterface;
use Nosto\Types\MarkupableInterface;

/**
 * Category object for tagging
 */
class Category extends AbstractObject implements
    CategoryInterface,
    MarkupableInterface,
    HtmlEncodableInterface
{
    use HtmlEncoderTrait;

    /**
     * @var string id
     */
    private $id;

    /**
     * @var string parentId
     */
    private $parentId;

    /**
     * @var string title
     */
    private $title;

    /**
     * @var string url
     */
    private $url;

    /**
     * @var string path
     */
    private $path;

    /**
     * @return string
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * @param string $id
     */
    public function setId($id)
    {
        $this->id = $id;
    }

    /**
     * @return string
     */
    public function getParentId()
    {
        return $this->parentId;
    }

    /**
     * @param int|string $parentId
     */
    public function setParentId($parentId)
    {
        $this->parentId = $parentId;
    }

    /**
     * @return string
     */
    public function getTitle()
    {
        return $this->title;
    }

    /**
     * @param string $title
     */
    public function setTitle($title)
    {
        $this->title = $title;
    }

    /**
     * @return string
     */
    public function getPath()
    {
        return $this->path;
    }

    /**
     * @param string $path
     */
    public function setPath($path)
    {
        $this->path = $path;
    }

    /**
     * @return string
     */
    public function getUrl()
    {
        return $this->url;
    }

    /**
     * @param string $url
     */
    public function setUrl($url)
    {
        $this->url = $url;
    }

    /**
     * @return string
     */
    public function getMarkupKey()
    {
        return 'nosto_category';
    }
}
